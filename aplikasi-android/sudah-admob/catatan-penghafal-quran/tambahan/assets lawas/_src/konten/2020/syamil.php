**Menghafal Al-Quran dengan Rileks**

_Syamil Muwahiduddin_

Assalamualaikum warahmatullahi wabarakatuh. Wahai para pembaca budiman, sekarang saya akan menjelaskan tentang judul di atas.

Menghafal Al-Quran adalah sebuah kebutuhan bagi setiap jiwa yang beriman pada Allah. Dengan menghafal Al-Quran, kita bisa mendapatkan syafaat dan memberi mahkota pada orang tua. 

Banyak yang bilang menghafal Quran itu SUSAH! Bahkan, saya sendiri merasa seperti itu sampai sekarang (SMP kelas 2). Kadang merasa iri dengan yang hafalannya cepat. Kalau ujian, STRES, depresi. Mungkin para pembaca juga seperti itu. TAPI INGAT! Allah pernah berfirman:

> Dan sungguh, Kami mudahkan Al-Quran untuk peringatan. Maka, adakah yang ingin mengambil pelajaran? (54/17)

Dan Allah pernah berfirman:

> Allah tidak membebebani seseorang kecuali sesuai dengan kesanggupannya (2/286)

Allah sendiri yang bilang Al-Quran itu mudah tapi kenapa kita merasa susah? Penyebabnya...

IBLIS!

Laknat Allah untuknya, dialah penyebab utama kita susah menghafal. Ada aja cara iblis menggoda kita dan kita pun jadi memiliki beberapa faktor:

- Penuh dosa
- Malas
- Mudah nyerah
- Tidak fokus
- Lemah mental

