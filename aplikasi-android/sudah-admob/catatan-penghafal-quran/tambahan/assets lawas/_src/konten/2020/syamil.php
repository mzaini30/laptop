Anggaplah target kita 6 halaman kita menghafal, capek, lelah, tapi tidak mencapai target. Mood kita langsung terjun bebas, dan kita jadi depresi. Ungkapannya: My mood is like roller coaster; terasa ulang dari nol.

NAH! Ini yang harus dihindari. Seharusnya, kalau gagal, ya diulang. Kalau depresi ya nggak bakal berubah karena pasti endingnya frustasi. Begitu juga dalam hal lain.

Kalau kita banyak masalah, kita selalu mempersepsikan orang lain lebih beruntung padahal belum tentu.

Ada istilah tidak ada yang sempurna, dan itu benar. Setiap orang ada masalahnya. Bahkan mungkin lebih banyak dari faktur yang saya sebutkan.

Tapi ingat, tidak ada yang terbebas dari masalah. Superhero aja punya masalah. Yang penting adalah KAU! Yakin bisa menanganinya dengan baik.

Mungkin, para pembaca pernah merasakan:

MY NEGATIVE SELF TALK

Yaitu pikiran-pikiran yang membuat stres. NAH sekarang saya meminta para pembaca ubah jadi kebalikannya; POSITIF!

Haruskah aku stres bila keinginan tidak tercapai? Tidak perlu!

Stres itu wajar. Kita bukan malaikat. Yang tepenting, usaha kita meminimalisir masalah dan apa yang kau lakukan agar tidak makin stres.

Coba para pembaca curhat pada seseorang. Insya Allah, hati lebih plong tapi ingat, janganlah salah pilih orang.

Tempat ternyaman

Kalau para pembaca bayangin hal indah, mungkin bisa senyum sendiri. Seketika rileks dan melupakan masalah. Iya kan?

Jadi, silahkan bayangkan apapun yang bikin rileks.

MY MANTRA

Kata-kata atau kalimat tertentu bisa jadi senjata untuk melawan pikiran negatif dan memompa semangat baru. Para pembaca juga bisa membuatnya.

Contoh:

- I can do it. I'll do my best
- Be strong. I'm fearless, strong and happy

Jika disebutkan terus-menerus, Insya Allah perubahan akan terjadi

Superhero inside me

Setiap orang punya potensi masing-masing yang seringkali muncul tanpa disangka-sangka. Ada orang berpikir "Ah, nggak bisa!" Eh, jadi bisa. Mari, coba ingat-ingat pengalaman masa lalu mengenai keberhasilan dalam masalah papun.

BAGAIMANA?

Terbukti bahwa pasti ada superhero dalam diri seseorang termasuk kamu. Jadi, papun yang kamu hadapi, just smile dan yakin pada diri sendiri.

Move on!

Move on dari masalah kalau selesai ya selsesai. Tutup buku, buka buku baru. Buat apa mikirin kelamaan? Bikin susah aja!

Kau yang mengatur tujuanmu dan kau tidak butuh sihir untuk itu dan tidak jalan pintas untuk menyelesaikan masalahmu.

Coba pikirkan peristiwa masa lalu ketika kamu bisa move on dari masalahmu. Pasti dalam hidupmu, kamu pernah mengalami masalah dan kamu bisa lewati dengan baik.

Jadi, kalau punya masalah, yang harus dilakukan: relaks >> berpikir positif >> cari masukan >> buat keputusan >> move on. Karena sekarang kamu sudah tau cara mengatasi dengan baik dan tepat, maka...

Jangan takut dan menghindar dalam menghadapi masalah.

Para pembaca budiman, ini terakhir yang saya sampaikan. Menghafal Al-Quran itu tidak bisa langsung hafal. Dibutuhkan murajaah, juga dibutuhkan kesabaran dan niat, ikhlas. Tidak ada yang instan di dunia ini. Mi instan aja butuh waktu 3 menit untuk siap dimakan. Begitu juga Quran. Ayo semangat, jangan putus asa. Hidup jangan dibawa stres (kayak sya). Jangan nunggu dunia membaik, baru bisa bahagia. Berbahagialah maka dunia akan membaik dengan sendirinya. Saya yakin semua orang pasti bisa jadi versi diri yang lebih baik. iPhone 11 takkan lahir tanpa iPhone 6, dan iPhone 6 takkan lahir tanpa iPod. Tapi tetap harus ada kemauan dari diri sendiri dulu dan yakin pada diri sendiri. Sekian. Wassalamualaikum warahmatullahi wabarakatuh.
