**Kemuliaan Al-Quran**

_Abdurrahman Al-Zuhdi_

Apakah kau tau apa itu kata sebuah perjuangan? Sebuah perjuangan tidak mesti berperang, berjihad, dan melawan orang yang jahat. Di dalam hadits dikatakan bahwa menuntut ilmu juga bisa dibilang sebuah perjuangan.

Apakah kau tau menuntut ilmu itu tidak semudah yang kau bayangkan? Jika kau ingin menuntut ilmu itu membutuhkan dana, guru, dan lain-lain. Dan itu semua tidaklah mudah.

Dan asalkan kau tau ilmu yang paling mulia adalah Al-Quran. Dan belajar Al-Quran itu banyak sekali pahalanya. Di dalam sebuah hadits dikatakan, Rasulullah bersabda: Aku tidak katakan alif lam mim 1 huruf, tetapi alif 1 huruf, lam 1 huruf, mim 1 huruf, dan 1 huruf diganjar 10 pahala.

Kalau kita mau berpkir lebih dalam, kita bisa bayangkan hanya dengan membaca basmalah, kita bisa mendapatkan kurang lebih 11 huruf, 170 kebaikan dan bagaimana kalau yang kita baca 1 juz atau lebih, berapa pahala yang didapat? Subhanallah.

Sekarang coba bayangkan, betapa beruntungnya orang yang membaca dan menghafal Al-Quran. Satu halaman itu jika dibaca sekitar 2 menit saja, maka apa ruginya membaca Al-Quran hanya perlu waktu yang sedikit bisa mendapatkan pahala yang berlimpah.

Maka, marilah semua, mulai sekarang, mulailah istiqomahkan diri dalam mempelajari dan mendalami Al-Quran, masih ada banyak sekali kebaikan dan pahala yang telah disiapkan oleh Allah Taala, jangan pernah menyerah dalam membaca dan menghafalkan Al-Quran.

Oke, sebagai penutup, saya akan memberikan salah satu pepatah tua: Carilah ilmu sampai negeri Cina. Carilah ilmu sampai ujung dunia. Sekian dari saya, wal akhiril kalam tsummassalamulaikum warahmatullahi wabarakatuh.
