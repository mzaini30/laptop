**Menghafal Al-Quran Itu Mudah**

_Dzakir Hawary Arbie_

Menghafal Al-Quran itu sulit tanpa usaha tapi dengan adanya usaha jadi mudah.

Menghafal Al-Quran itu dari yang sulit, sulit, sulit, mudah. Dari sulit ke mudah itu ada usaha dan niat. Tanpa adanya niat dan usaha, maka jadi sulit menghafal.

Awal-awal menghafal Al-Quran mudah awal-awalnya bagi saya. Tapi saat ketemu halaman atau ayat yang susah, saya merasa jadi putus asa. Tapi, karena saya dapat dukungan dan motivasi dari orang tua, teman, ustadz, dan ustadzah, semangat untuk menghafal Al-Quran.

Jadi, kalian itu jangan putus asa dalam menghafal Al-Quran. Walaupun kalian dapat ayat yang susah tapi jika kalian terus berusaha maka ayat yang tadinya susah jadi mudah.

Menghafal Al-Quran itu seperti kita menaiki gunung yang tinggi. Saat melihat gunung yang tinggi, sudah putus asa untuk ke puncak. Tapi, dia selalu menaiki gunung. Dan saat sampai di puncak, maka dia akan merasakan kebahagiaan. Nah, gitu juga menghafal Quran dari susah ke mudah.

Semangat jangan putus asa karena menghafal Al-Quran akan bahagia di dunia dan di akhirat.

